a = True
b = False

print('\n AND logic: ')
print('a and a' , a and a)
print('a and b' , a and b)
print('b and b' , b and b)

print('\n OR logic: ')
print('a or a' , a or a)
print('a or b' , a or b)
print('b or b' , b or b)

print('\n NOT logic: ')
print('a =' , a , '\t not a =' , not a)
print('b = ' , b , '\t not b =' , not b)

